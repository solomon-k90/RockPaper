// Function to check if a string starts with 'java'
function checkStartsWithJava(str) {
    if (str.startsWith('java')) {
        console.log('The string starts with "java".');
    } else {
        console.log('The string does not start with "java".');
    }
}

// Example usage:
const exampleString = 'javaScript is fun';
checkStartsWithJava(exampleString);

const anotherString = 'python is great';
checkStartsWithJava(anotherString);
